// Lab 01 - Bài 02 Vẽ các hình 

#include <iostream>

using namespace std;

int main(int argc, const char **argv) {
	// Vẽ hình bàn cờ
	cout << '\t' << "* * * * * " << endl;
	cout << '\t' << " * * * * *" << endl;
	cout << '\t' << "* * * * * " << endl;
	cout << '\t' << " * * * * *" << endl;
	cout << '\t' << "* * * * * " << endl;

	cout << '\t' << "==========" << endl;

	// Vẽ viền hình chữ nhật
	cout << '\t' << "* * * * * " << endl;
	cout << '\t' << "*       * " << endl;
	cout << '\t' << "*       * " << endl;
	cout << '\t' << "*       * " << endl;
	cout << '\t' << "* * * * * " << endl;

	cout << '\t' << "==========" << endl;

	// Vẽ hình tam giác ngược
	cout << '\t' << "* * * * *" << endl;
	cout << '\t' << " *     * " << endl;
	cout << '\t' << "  *   *  " << endl;
	cout << '\t' << "   * *   " << endl;
	cout << '\t' << "    *    " << endl;

	// Trả giá trị về cho hàm main
	return 0;
}